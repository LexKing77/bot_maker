# coding=utf-8
import telebot
import config

from telebot import types

bot = telebot.TeleBot(config.TOKEN)

# –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–æ–±—â–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –∫–æ–º–∞–Ω–¥—ã, –≤ –Ω–∞—à–µ–º —Å–ª—É—á–∞–µ —ç—Ç–æ –∫–æ–º–∞–Ω–¥–∞ /start
@bot.message_handler(commands=['start'])
def startpage(message):
    # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É, —Å –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–π –∫–Ω–æ–ø–∫–æ–π "–ù–∞—á–∞—Ç—å"
    startmenu = types.ReplyKeyboardMarkup(True, True)
    startmenu.row('–ù–∞—á–∞—Ç—å')
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
    bot.send_message(message.chat.id, '–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!‚úãüèª \n–Ø –±–µ—Å–ø–ª–∞—Ç–Ω—ã–π –æ–±—Ä–∞–∑–æ–≤–∞—Ç–µ–ª—å–Ω—ã–π –±–æ—Ç!üë®üèª‚Äçüè´\n–ë–ª–∞–≥–æ–¥–∞—Ä—è –Ω–∞—à–∏–º –∫—É—Ä—Å–∞–º, –≤—ã —Å–º–æ–∂–µ—Ç–µ –Ω–∞–π—Ç–∏ –ª—é–±–∏–º—É—é –ø—Ä–æ—Ñ–µ—Å—Å–∏—é, –Ω–∞—É—á–∏—Ç—Å—è –∑–∞—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –¥–µ–Ω—å–≥–∏ –∏ –ø—Ä–æ—Å—Ç–æ –æ–±—Ä–µ—Å—Ç–∏ –ø–æ–ª–µ–∑–Ω—ã–µ –Ω–∞–≤—ã–∫–∏!', reply_markup=startmenu)

#–î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–æ–±—â–µ–Ω–∏–π, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–æ–≤–µ—Ä—è–µ—Ç —Ç–∏–ø —Å–æ–æ–±—â–µ–Ω–∏—è "–¢–µ–∫—Å—Ç"
@bot.message_handler(content_types=['text'])
def main(message):
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–ù–∞—á–∞—Ç—å", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    if message.text == '–ù–∞—á–∞—Ç—å':
        # –ú—ã –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π "send" –º–µ—Ç–æ–¥ "–û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è"
        send = bot.send_message(message.chat.id, '–í–≤–µ–¥–∏—Ç–µ –≤–∞—à–µ –∏–º—è:')
        # –ó–¥–µ—Å—å –º—ã –¥–µ–ª–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π —à–∞–≥, –∫–æ—Ç–æ—Ä—ã–π –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –±—É–¥–µ—Ç –æ—Ç–ø—Ä–∞–≤–ª—è—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é "send", –∞ –ø–æ—Ç–æ–º –ø–µ—Ä–µ—Ö–æ–¥–∏—Ç –∫ —Å–ª–µ–¥—É—é—â–µ–π —Ñ—É–Ω–∫—Ü–∏–∏ –ø–æ–¥ –Ω–∞–∑–≤–∞–Ω–∏–µ–º "next1"
        bot.register_next_step_handler(send, next1)
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–ë–∏–∑–Ω–µ—Å", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    elif message.text == '–ë–∏–∑–Ω–µ—Å':
        if business == 'business':
            # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
            option1 = types.ReplyKeyboardMarkup(True, False)
            option1.row('–ö—É—Ä—Å1')
            option1.row('–ö—É—Ä—Å2')
            option1.row('–ö—É—Ä—Å3')
            option1.row('–ö—É—Ä—Å4')
            option1.row('–ù–∞–∑–∞–¥')
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
            bot.send_message(message.chat.id, '–í—ã–±–µ—Ä–∏—Ç–µ –∫—É—Ä—Å:', reply_markup=option1)
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–ü—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    elif message.text == '–ü—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ':
        if business == 'business':
            # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
            option2 = types.ReplyKeyboardMarkup(True, False)
            option2.row('C#')
            option2.row('Python')
            option2.row('C++')
            option2.row('Delphi')
            option2.row('–ù–∞–∑–∞–¥')
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
            bot.send_message(message.chat.id, '–í—ã–±–µ—Ä–∏—Ç–µ —è–∑—ã–∫ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏—è:', reply_markup=option2)
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–õ–∞–π—Ñ–∞–∫–∏", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    elif message.text == '–õ–∞–π—Ñ–∞–∫–∏':
        if business == 'business':
            # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
            option3 = types.ReplyKeyboardMarkup(True, False)
            option3.row('–õ–∞–π—Ñ–∞–∫1')
            option3.row('–õ–∞–π—Ñ–∞–∫2')
            option3.row('–õ–∞–π—Ñ–∞–∫3')
            option3.row('–õ–∞–π—Ñ–∞–∫4')
            option3.row('–ù–∞–∑–∞–¥')
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
            bot.send_message(message.chat.id, '–í—ã–±–µ—Ä–∏—Ç–µ –ª–∞–π—Ñ–∞–∫:', reply_markup=option3)
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–§–∏—Ç–Ω–µ—Å", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    elif message.text == '–§–∏—Ç–Ω–µ—Å':
        if business == 'business':
            # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
            option4 = types.ReplyKeyboardMarkup(True, False)
            option4.row('–ü–∏—Ç–∞–Ω–∏–µ')
            option4.row('–£–ø—Ä–∞–∂–Ω–µ–Ω–∏—è')
            option4.row('–ü—Ä–µ—Å—Å')
            option4.row('–§–∏—Ç–Ω–µ—Å –∫–ª—É–±—ã')
            option4.row('–ù–∞–∑–∞–¥')
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
            bot.send_message(message.chat.id, '–í—ã–±–µ—Ä–∏—Ç–µ:', reply_markup=option4)
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∫–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–∞–º –ø—Ä–∏—à–ª–æ, –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ –∫–æ–¥ –≤—ã–≥–ª—è–¥–∏—Ç —Ç–∞–∫ "–ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ—Ç–æ—Ä—ã–π –æ—Ç–ø—Ä–∞–≤–∏–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –†–ê–í–ï–ù –°–õ–û–í–£ "–ù–∞–∑–∞–ª", —Ç–æ —É –Ω–∞—Å –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ"
    elif message.text == '–ù–∞–∑–∞–¥':
        # –¢–∞–∫ –¥–µ–ª–∞–µ—Ç—Å—è, –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –ø–µ—Ä–µ–π—Ç–∏ –∫ —Ñ—É–∫–Ω—Ü–∏–∏ —Å –Ω–∞–∑–∞–≤–∞–µ–Ω–∏–µ–º "next2"
        next2(message)

# –°–æ–∑–¥–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é, –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –æ—Ç–≤–µ—Ç–∏—Ç—å –Ω–∞ –ø–µ—Ä–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ "–í–≤–µ–¥–∏—Ç–µ –≤–∞—à–µ –∏–º—è:"
def next1(message):
    # –ú—ã –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π "send" –º–µ—Ç–æ–¥ "–û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è"
    send = bot.send_message(message.chat.id, '–û—á–µ–Ω—å –ø—Ä–∏—è—Ç–Ω–æ {name}, –≤–≤–µ–¥–∏—Ç–µ –≤–∞—à –≤–æ–∑–≤—Ä–∞—Å—Ç:'.format(name=message.text))
    # –ó–¥–µ—Å—å –º—ã –¥–µ–ª–∞–µ–º —Å–ª–µ–¥—É—é—â–∏–π —à–∞–≥, –∫–æ—Ç–æ—Ä—ã–π –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –±—É–¥–µ—Ç –æ—Ç–ø—Ä–∞–≤–ª—è—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é "send", –∞ –ø–æ—Ç–æ–º –ø–µ—Ä–µ—Ö–æ–¥–∏—Ç –∫ —Å–ª–µ–¥—É—é—â–µ–π —Ñ—É–Ω–∫—Ü–∏–∏ –ø–æ–¥ –Ω–∞–∑–≤–∞–Ω–∏–µ–º "next2"
    bot.register_next_step_handler(send, next2)

# –°–æ–∑–¥–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é, –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –æ—Ç–≤–µ—Ç–∏—Ç—å –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ "–û—á–µ–Ω—å –ø—Ä–∏—è—Ç–Ω–æ {name},–í–≤–µ–¥–∏—Ç–µ –≤–∞—à –≤–æ–∑–≤—Ä–∞—Å—Ç:"
def next2(message):
    global business
    business = 'business'
    # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
    option = types.ReplyKeyboardMarkup(True, False)
    option.row('–ë–∏–∑–Ω–µ—Å')
    option.row('–ü—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ')
    option.row('–õ–∞–π—Ñ–∞–∫–∏')
    option.row('–§–∏—Ç–Ω–µ—Å')
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä
    bot.send_message(message.chat.id, '–•–æ—Ä–æ—à–æ, –≤—ã–±–µ—Ä–∏—Ç–µ —Ç–µ–º–∞—Ç–∏–∫—É –∫—É—Ä—Å–æ–≤?', reply_markup=option)


bot.polling()
